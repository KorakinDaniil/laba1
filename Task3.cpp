// К-ичные числа. Среди чисел в системе счисления с основанием K (2 ≤ K ≤ 10) определить сколько
// имеется чисел из N (1 < N < 20, K + N < 26) разрядов таких, что в их записи не содержится
// два и более подряд идущих нулей. Для того, чтобы избежать переполнения, ответ
// представьте в виде вещественного числа.

#include <iostream>
#include <fstream>
#include <vector>
#include <string>

//FIX_ME: using namespace std; запрещено стандартом
//using namespace std;

int Task3()
{
    setlocale(LC_ALL, "ru");

    int K = 0, N = 0;
    //FIX_ME: нет проверки на дурака
    std::cout << "Введите K и N: ";
    std::cin >> K >> N;

    // Проверка корректности введенных данных
    if (K < 2 || K > 10)
    {
        std::cout << "Ошибка: K должно быть в диапазоне от 2 до 10" << std::endl;
        return 1;
    }

    if (N <= 1 || N >= 20)
    {
        std::cout << "Ошибка: N должно быть в диапазоне 1 < N < 20" << std::endl;
        return 1;
    }

    if (K + N >= 26)
    {
        std::cout << "Ошибка: K + N должно быть меньше 26" << std::endl;
        return 1;
    }

    // DP[i][j] - количество чисел длины i, заканчивающихся на цифру j
    std::vector<std::vector<double>> Dp(N + 1, std::vector<double>(K, 0.0));

    // База: числа длины 1
    for (int j = 0; j < K; ++j)
    {
        Dp[1][j] = 1.0;
    }

    // Заполнение таблицы динамического программирования
    for (int i = 2; i <= N; ++i)
    {
        // Для чисел, заканчивающихся на 0
        for (int j = 1; j < K; ++j)
        {
            Dp[i][0] += Dp[i - 1][j];
        }

        // Для чисел, заканчивающихся на ненулевые цифры
        for (int j = 1; j < K; ++j)
        {
            for (int l = 0; l < K; ++l)
            {
                Dp[i][j] += Dp[i - 1][l];
            }
        }
    }

    // Суммируем все числа длины N (не начинающиеся с 0)
    double Result = 0.0;
    for (int j = 1; j < K; ++j)
    {
        Result += Dp[N][j];
    }

    std::cout << "Ответ: " << Result << std::endl;

    return 0;
}
